print "Digite um número inteiro: "
a = gets.chomp

print "Digite o segundo número inteiro: "
b = gets.chomp

puts "A soma entre eles: #{a.to_i + b.to_i}"
puts "A subtração entre eles: #{a.to_i - b.to_i}"
puts "A multiplicação entre eles: #{a.to_i * b.to_i}"
puts "A divisão entre eles: #{a.to_i / b.to_i}"
puts "A módulo entre eles: #{a.to_i % b.to_i}"